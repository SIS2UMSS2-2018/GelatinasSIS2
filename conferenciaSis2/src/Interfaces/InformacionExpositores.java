    // Llena los arraylists con informacion de la base de datos
    private void llenarArrayListsConNombres(){
        ResultSet rs = con.obtenerNombreApellidoDeExpositores();
        nombres = new ArrayList<String>();
        apellidos = new ArrayList<String>();
        try{
            while ( rs.next() ){
                String nombre, apellido;
                nombre = rs.getString("nombre_expo");
                apellido = rs.getString("apellido_expo");
                nombres.add( nombre );
                apellidos.add( apellido );
            }
        }
        catch (SQLException ex){
            Logger.getLogger(Consultas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    // Llena el jComboBox con nombres y apellidos
    private void llenarJComboBox(){
        for (int i = 0; i < nombres.size(); i++){
            String item;
            item = nombres.get(i) + " " + apellidos.get(i);
            jComboBoxNombresExpositores.addItem(item);
        }
    }
    
    public InformacionExpositores(Consultas co) {
        initComponents();
        this.setLocationRelativeTo(null);
        con = co;
        llenarArrayListsConNombres();
        llenarJComboBox();
        AutoCompleteDecorator.decorate(jComboBoxNombresExpositores);
    }

